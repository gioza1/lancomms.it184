    public void append(String str) {
        logs.append(str);
        logs.setCaretPosition(logs.getText().length() - 1);
    }    

    
    public void displayMessages(){
        ArrayList<String> messages = cvobj.getMessages();
        for(String message:messages){
            append(message);
        }

    }    
    
    
